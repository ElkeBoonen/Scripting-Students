def sum(x,y):
    """ Returns sum of two numbers
    Parameters:
        x (int): a number
        y (int): a number
    Returns:
        sum of x and y
    """
    return x+y

def average(x,y):
    """ Returns average of two numbers
    Parameters:
        x (int): a number
        y (int): a number
    Returns:
        average of x and y
    """
    return (x+y)/2

def power(x,y):
    """ Returns x to the power of y
    Parameters:
        x (int): a number
        y (int): a number
    Returns:
        x^y
    """
    return x**y